package org.apache.cordova.Idbox_Plugin.util;

public class SubHeaderText {
  String subHeaderText;
  String[] subHeaderTextArr;
  String drawable;

  public String getSubHeaderText() {
    return subHeaderText;
  }

  public void setSubHeaderText(String subHeaderText) {
    this.subHeaderText = subHeaderText;
  }

  public String[] getSubHeaderTextArr() {
    return subHeaderTextArr;
  }

  public void setSubHeaderTextArr(String[] subHeaderTextArr) {
    this.subHeaderTextArr = subHeaderTextArr;
  }

  public String  getDrawable() {
    return drawable;
  }

  public void setDrawable(String drawable) {
    this.drawable = drawable;
  }
}
